let text = 'Lorem ipsum dolor sit amet'

// startsWith()
console.log(text.startsWith('L')) // Verifica se a string começa com o parâmetro informado no método startsWith()

//OU

console.log(text.startsWith('rem', 2))

//endsWith()
console.log(text.endsWith('t')) // Verifica se a string termina com o parâmetro informado no método endsWith()

//OU

console.log(text.endsWith('ame', 25))

// repeat()
console.log('lo'.repeat(10)) // Repete a string na quantidade de vezes informada por parâmetro no método repeat()

// includes()
console.log(text.includes('L')) // Verifica se a string contém o parâmetro informado no método includes()